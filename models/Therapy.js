const mongoose = require('mongoose');

const therapySchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  category: {
    type: String,
    enum: ['anxiety', 'depression', 'general'],
    required: true
  },
  duration: {
    type: Number, // in weeks
    required: true
  },
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  }
}, {
  timestamps: true
});

module.exports = mongoose.model('Therapy', therapySchema); 